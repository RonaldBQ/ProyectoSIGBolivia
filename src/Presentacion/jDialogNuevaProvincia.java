    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (dptoSeleccionado == null) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar el departamento");
        } else {
            Provincia dts = new Provincia();
            dptoSeleccionado.setNombre(cboDepto.getSelectedItem().toString());
            dts.setNombre(txtProvincia.getText());
            dts.setIdDepartamento(dptoSeleccionado.getIdDepartamento());
            if (objProv.insertarProvincia(dts)) {
               JOptionPane.showMessageDialog(rootPane, "Se registr√≥ la provincia correctamente");
                limpiaCampos();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se pudo registrar", "Error", ERROR);
                limpiaCampos();
                this.dispose();
            }
        }    
    }//GEN-LAST:event_btnGuardarActionPerformed

